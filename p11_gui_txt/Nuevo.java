    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        nombre = txtNombre.getText();
        tipo = cbxTipo.getSelectedItem().toString();
        id = txtId.getText();
        
        if(nombre.isEmpty() || tipo.isEmpty()){
            JOptionPane.showMessageDialog(this, "Ebe rellenar todos los campos", "Mensaje", JOptionPane.ERROR_MESSAGE);
        }else{
            LogicaTexto logica = new LogicaTexto();
            
            logica.guardarAnimal(nombre, tipo, id);
            this.dispose();
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

